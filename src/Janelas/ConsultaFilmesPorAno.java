    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText("");
        ControleFilmes cnt = new ControleFilmes();
        ConsultaFilmesPorAno.ItemAno ia = (ConsultaFilmesPorAno.ItemAno) jComboBox1.getSelectedItem();
        ArrayList lista = cnt.getPorAno(ia.ano);
        for (int i = 0; i < lista.size(); i++) {
            Filmes flm = (Filmes) lista.get(i);
            String detalhe = flm.getId() + "\t"
                    + flm.getNome() + "\t"
                    + flm.getAno()+ "\t ("
                    + flm.getCategoria().getNome() + ") \n";
            jTextArea1.append(detalhe);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

