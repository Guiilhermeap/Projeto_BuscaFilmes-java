    public ConsultaFilmesPorAno(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();

        ControleFilmes cc = new ControleFilmes();

        ArrayList as = cc.getAnos();
        itens = new ItemAno[as.size()];
        for (int i = 0; i < as.size(); i++) {
            itens[i] = new ItemAno();
            int a = (Integer) as.get(i);
            itens[i].ano = a;
        }
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(itens));
    }

