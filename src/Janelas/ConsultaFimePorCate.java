    public ConsultaFimePorCate(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        
        initComponents();
        
        ControleCategoria cc = new ControleCategoria();

        ArrayList cids = cc.getAll();
        itens = new ItemCategoria[cids.size()];
        for (int i = 0; i < cids.size(); i++) {
            Categoria cate = (Categoria) cids.get(i);
            itens[i] = new ItemCategoria();
            itens[i].nome = cate.getNome();
            itens[i].id = cate.getId();
        }
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(itens));
    }

