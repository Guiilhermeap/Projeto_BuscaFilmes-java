    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText("");
        ControleFilmes cnt = new ControleFilmes();
        ItemCategoria ic = (ItemCategoria) jComboBox1.getSelectedItem();
        ArrayList lista = cnt.getPorCategoria(ic.id);
        for (int i = 0; i < lista.size(); i++) {
            Filmes flm = (Filmes) lista.get(i);
            String detalhe = flm.getId() + "\t"
                    + flm.getNome() + "\t"
                    + flm.getAno() + "\t" + "(" 
                    + flm.getCategoria().getNome()+ ")" + "\n";
            jTextArea1.append(detalhe);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

